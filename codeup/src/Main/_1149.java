package Main;

import java.util.Scanner;
public class _1149 {

		public class Main {
		    public static void main(String[] args) {
		        Scanner scanner = new Scanner(System.in);
		        
		        // 두 정수 입력 받기
		        int a = scanner.nextInt();
		        int b = scanner.nextInt();
		        
		        // 더 큰 수 출력
		        if (a > b) {
		            System.out.println(a);
		        } else {
		            System.out.println(b);
		        }

		        
		        scanner.close();
		    }

				
			}
		}
	


